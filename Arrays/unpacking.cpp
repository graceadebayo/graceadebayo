#include <iostream>

using namespace std;

int main()
{
    // unpacking arrays
    int numbers[] = {1, 2, 3, 4, 5};
    int a = numbers[0];
    int b = numbers[1];
    int c = numbers[2];
    int d = numbers[3];
    int e = numbers[4];
    cout << "a: " << a << endl;
    cout << "b: " << b << endl;
    cout << "c: " << c << endl;
    cout << "d: " << d << endl;
    cout << "e: " << e << endl;

    return 0;
}